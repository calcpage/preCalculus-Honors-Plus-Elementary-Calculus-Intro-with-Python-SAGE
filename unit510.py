show("510 U-Substitution")
show("integrate(x^5,x)=",integrate(x^5,x))
show("integrate((x+1)^5,x)=",integrate((x+1)^5,x))
show("integrate((2*x+1)^5,x)=",integrate((2*x+1)^5,x))
show("integrate(sqrt(4*x-1),x)=",integrate(sqrt(4*x-1),x))
show("integrate(sin(x)^4*cos(x),x)=",integrate(sin(x)^4*cos(x),x))
show("integrate(x^2*sqrt(x^3+1),x)=",integrate(x^2*sqrt(x^3+1),x))
show("integrate(x^2*sqrt(x^3+1),x,0,2)=",integrate(x^2*sqrt(x^3+1),x,0,2))
